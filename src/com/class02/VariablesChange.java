package com.class02;

public class VariablesChange {

	public static void main(String[] args) {

		// values of variables can change

		String name = "Bunny";
		String lastName = "White";
		lastName = "Johnson";

		char grade = 'B';

		grade = 'A';

		// creating a variable and assigning the value
		String city = "Fairfax";

		// changing value of city from Fairfax to New York
		city = "New York";

		String state = "VA";
		String phoneNumber = "7036665555";

		// printing values of variables

		System.out.println(city);
		System.out.println(lastName);

	}

}
