package com.class02;

public class VariablesDeclaration {

	public static void main(String[] args) {

		// Different ways to create/declare variables

		// 1. Create variable and assign value

		int num = 28;

		// 2. Create variable using 2 steps

		int y;// 1. declaring variable
		y = 12; // 2/assign value

		// declare multiple variables one by one
		int a;
		int b;
		int c;
		int d;

		// or declare multiple variables all together
		int x, m, z, w, v;
		x = 10;
		m = 20;
		z = 30;
		w = 40;
		v = 50;

		String name = "John";

		System.out.println(name);

	}

}
