package com.class07;

public class CompoundOperators {

	public static void main(String[] args) {
		
		int a=10;
		a=a+10;
		
		System.out.println(a);

		int b=100;
		b=b+100;
		
		System.out.println(b);
		
		int c=100; //shorter way
		c+=100; //c=c+100;
		System.out.println(c);
		
		
		c+=35; //c=c+35;
		System.out.println(c);
		
		c+=50;
		System.out.println(c);
		
		int d=30;
		d-=10;
		System.out.println(d);
		
		d+=50;
		System.out.println(d);
		
		d/=5;
		System.out.println(d);
		
		d%=4;
		System.out.println(d);
		
		
	}

}
