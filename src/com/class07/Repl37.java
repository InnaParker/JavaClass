public class Repl37 {

	public static void main(String[] args) {
		
		/*Create a while loop that prints out numbers from 0 to 15.
		 * Must include the number 0 in the output decrement by 1 
		 * example(a--) */

		int a=15;
		
		while (a>=0) {
			System.out.println(a);
			a--;
		}
	}

}
