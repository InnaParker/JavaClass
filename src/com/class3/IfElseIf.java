package com.class3;

public class IfElseIf {

	public static void main(String[] args) {

		int num1 = 23;
		int num2 = 14;

		if (num1 > num2) {
			System.out.println("Num1 is larger than Num2"); // if block

		} else if (num1 == num2) {
			System.out.println("Num1 is equal to Num2"); // else block

		} else {
			System.out.println("Num1 is smaller than Num2");
		}
		System.out.println("I am done wtih conditon");

	}

}
