package com.class08;

public class Repl46 {

	public static void main(String[] args) {
		
		/* write a program with use of for loop which will print multiples
		 * of 3 between 3 and 20. Must not include number 20 in the output.
		 */

		for (int a=1; a<20; a++) {
			if (a%3==0) {
				System.out.println(a);
			}
			
		}
	}

}
