package com.class08;

public class Repl43 {

	public static void main(String[] args) {

		/*
		 * create a for loop that prints out even numbers from 0 to 10. must decrement
		 * by 1 (i--). Must not include 0 in the output.
		 */

		for (int a = 10; a > 0; a--) {

			System.out.println(a);
		}

	}

}
