public class Repl45 {

	public static void main(String[] args) {
		
		/* create for loop that prints odd numbers from 5 to 22.
		 * increment by 1 (i++(. Must not include 22 in the output.
		 */

		for (int a=5; a<22; a++) {
			
			if (a%2!=0) {
				System.out.println(a);
			}
		}
	}

}
