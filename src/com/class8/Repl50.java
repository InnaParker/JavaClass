package com.class8;

public class Repl50 {

	public static void main(String[] args) {
		
		/* write a program that prints the count down from 10 up to 1 string
		 * "Happy New Year!". Output:
		 * 10
		 * 9
		 * 
		 * 1
		 * Happy New Year!
		 */

		for (int count=10; count>=1; count--) {
			System.out.println(count);
		}
		System.out.println("Happy New Year!");
	}

}
