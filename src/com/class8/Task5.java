public class Task5 {

	public static void main(String[] args) {
		
		
		// Task 5 - 1st way. Print even numbers 20 to 50
	
		for (int z=20; z<=50; z++) {
			if (z%2!=0) {
				
			System.out.println(z);
			
		}

		}
		
		//Task 5 - 2nd way
		
		for (int x=20; x<=50; x+=2) {
			System.out.println(x);
			

	}

}
}
