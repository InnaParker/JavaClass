public class Repl44 {

	public static void main(String[] args) {
		
		/* create for loop that prints even numbers from 2 to 14.
		 * increment by 1 (i++). Must include number 14 in output.
		 */

		for (int a=2; a<=14; a++) {
			
			if (a%2==0) {
				System.out.println(a);
			}
		}
	}

}
